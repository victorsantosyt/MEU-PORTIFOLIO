function detectLocale(): Locale {
  const browserLang = navigator.language || (navigator as any).userLanguage || "en";
  const prefix = browserLang.split("-")[0].toLowerCase() as Locale;
  if (prefix in translations) return prefix;
  return "en";
}

interface I18nContextType {
  locale: Locale;
  t: (key: string) => string;
  setLocale: (locale: Locale) => void;
  availableLocales: { code: Locale; label: string }[];
}

const availableLocales: { code: Locale; label: string }[] = [
  { code: "en", label: "EN" },
  { code: "pt", label: "PT" },
  { code: "es", label: "ES" },
  { code: "fr", label: "FR" },
  { code: "de", label: "DE" },
  { code: "it", label: "IT" },
  { code: "ja", label: "JA" },
  { code: "zh", label: "ZH" },
  { code: "ko", label: "KO" },
  { code: "ru", label: "RU" },
];

const I18nContext = createContext<I18nContextType>({
  locale: "en",
  t: (key: string) => key,
  setLocale: () => {},
  availableLocales,
});

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [locale, setLocale] = useState<Locale>(detectLocale);

  const t = (key: string): string => {
    return translations[locale]?.[key] || translations.en[key] || key;
  };

  return (
    <I18nContext.Provider value={{ locale, t, setLocale, availableLocales }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => useContext(I18nContext);
